
public class Cioccolato {

    private String tipo;
    private int percentuale;
    private String aggiunte;
    private int costo;

    public Cioccolato(String tipo, int percentuale, String aggiunte, int costo) {
        this.tipo= tipo;
        this.percentuale=percentuale;
        this.aggiunte=aggiunte;
        this.costo= costo;
    }
}
